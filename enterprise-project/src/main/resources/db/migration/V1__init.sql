-- Crear tabla Empresa
CREATE TABLE Empresa (
    ID BIGINT PRIMARY KEY,
    CUIT VARCHAR(20),
    RAZON_SOCIAL VARCHAR(255),
    FECHA_ADHESION DATE,
    CREATED_DATE DATE,
    MODIFIED_DATE DATE,
    CREATED_BY VARCHAR(255),
    MODIFIED_BY VARCHAR(255),
    IS_ACTIVE BOOLEAN
);

